using NOIR.Application.Features.Orders.Commands.CompleteOrder;
using NOIR.Application.Features.Orders.DTOs;
using NOIR.Application.Features.Orders.Specifications;

namespace NOIR.Application.UnitTests.Features.Orders.Commands.CompleteOrder;

/// <summary>
/// Unit tests for CompleteOrderCommandHandler.
/// Tests order completion scenarios with mocked dependencies.
/// </summary>
public class CompleteOrderCommandHandlerTests
{
    #region Test Setup

    private readonly Mock<IRepository<Order, Guid>> _orderRepositoryMock;
    private readonly Mock<IUnitOfWork> _unitOfWorkMock;
    private readonly CompleteOrderCommandHandler _handler;

    private const string TestTenantId = "test-tenant";

    public CompleteOrderCommandHandlerTests()
    {
        _orderRepositoryMock = new Mock<IRepository<Order, Guid>>();
        _unitOfWorkMock = new Mock<IUnitOfWork>();

        _handler = new CompleteOrderCommandHandler(
            _orderRepositoryMock.Object,
            _unitOfWorkMock.Object);
    }

    private static CompleteOrderCommand CreateTestCommand(Guid? orderId = null)
    {
        return new CompleteOrderCommand(orderId ?? Guid.NewGuid());
    }

    private static Order CreateTestOrder(
        string orderNumber = "ORD-20250126-0001",
        string customerEmail = "customer@example.com",
        decimal subTotal = 100.00m,
        decimal grandTotal = 110.00m)
    {
        return Order.Create(orderNumber, customerEmail, subTotal, grandTotal, "VND", TestTenantId);
    }

    private static Order CreateDeliveredOrder(
        string orderNumber = "ORD-20250126-0001",
        string customerEmail = "customer@example.com")
    {
        var order = CreateTestOrder(orderNumber, customerEmail);
        order.Confirm();
        order.StartProcessing();
        order.Ship("TRACK-123", "GHTK");
        order.MarkAsDelivered();
        return order;
    }

    #endregion

    #region Success Scenarios

    [Fact]
    public async Task Handle_WithDeliveredOrder_ShouldCompleteOrderSuccessfully()
    {
        // Arrange
        var orderId = Guid.NewGuid();
        var existingOrder = CreateDeliveredOrder();
        var command = CreateTestCommand(orderId);

        _orderRepositoryMock
            .Setup(x => x.FirstOrDefaultAsync(
                It.IsAny<OrderByIdForUpdateSpec>(),
                It.IsAny<CancellationToken>()))
            .ReturnsAsync(existingOrder);

        _unitOfWorkMock
            .Setup(x => x.SaveChangesAsync(It.IsAny<CancellationToken>()))
            .ReturnsAsync(1);

        // Act
        var result = await _handler.Handle(command, CancellationToken.None);

        // Assert
        result.IsSuccess.Should().BeTrue();
        result.Value.Status.Should().Be(OrderStatus.Completed);
        result.Value.CompletedAt.Should().NotBeNull();

        _unitOfWorkMock.Verify(
            x => x.SaveChangesAsync(It.IsAny<CancellationToken>()),
            Times.Once);
    }

    [Fact]
    public async Task Handle_ShouldSetCompletedAtTimestamp()
    {
        // Arrange
        var orderId = Guid.NewGuid();
        var existingOrder = CreateDeliveredOrder();
        var command = CreateTestCommand(orderId);
        var beforeComplete = DateTimeOffset.UtcNow;

        _orderRepositoryMock
            .Setup(x => x.FirstOrDefaultAsync(
                It.IsAny<OrderByIdForUpdateSpec>(),
                It.IsAny<CancellationToken>()))
            .ReturnsAsync(existingOrder);

        _unitOfWorkMock
            .Setup(x => x.SaveChangesAsync(It.IsAny<CancellationToken>()))
            .ReturnsAsync(1);

        // Act
        var result = await _handler.Handle(command, CancellationToken.None);

        // Assert
        result.IsSuccess.Should().BeTrue();
        result.Value.CompletedAt.Should().NotBeNull();
        result.Value.CompletedAt.Should().BeOnOrAfter(beforeComplete);
        result.Value.CompletedAt.Should().BeOnOrBefore(DateTimeOffset.UtcNow.AddSeconds(1));
    }

    [Fact]
    public async Task Handle_ShouldReturnCorrectOrderDetails()
    {
        // Arrange
        var orderId = Guid.NewGuid();
        var existingOrder = CreateDeliveredOrder(
            orderNumber: "ORD-20250126-0042",
            customerEmail: "john@example.com");
        existingOrder.SetCustomerInfo(Guid.NewGuid(), "John Doe", "0901234567");
        var command = CreateTestCommand(orderId);

        _orderRepositoryMock
            .Setup(x => x.FirstOrDefaultAsync(
                It.IsAny<OrderByIdForUpdateSpec>(),
                It.IsAny<CancellationToken>()))
            .ReturnsAsync(existingOrder);

        _unitOfWorkMock
            .Setup(x => x.SaveChangesAsync(It.IsAny<CancellationToken>()))
            .ReturnsAsync(1);

        // Act
        var result = await _handler.Handle(command, CancellationToken.None);

        // Assert
        result.IsSuccess.Should().BeTrue();
        result.Value.OrderNumber.Should().Be("ORD-20250126-0042");
        result.Value.CustomerEmail.Should().Be("john@example.com");
        result.Value.CustomerName.Should().Be("John Doe");
        result.Value.Status.Should().Be(OrderStatus.Completed);
        result.Value.DeliveredAt.Should().NotBeNull();
        result.Value.CompletedAt.Should().NotBeNull();
    }

    [Fact]
    public async Task Handle_ShouldPreservePreviousTimestamps()
    {
        // Arrange
        var orderId = Guid.NewGuid();
        var existingOrder = CreateDeliveredOrder();
        var command = CreateTestCommand(orderId);

        _orderRepositoryMock
            .Setup(x => x.FirstOrDefaultAsync(
                It.IsAny<OrderByIdForUpdateSpec>(),
                It.IsAny<CancellationToken>()))
            .ReturnsAsync(existingOrder);

        _unitOfWorkMock
            .Setup(x => x.SaveChangesAsync(It.IsAny<CancellationToken>()))
            .ReturnsAsync(1);

        // Act
        var result = await _handler.Handle(command, CancellationToken.None);

        // Assert
        result.IsSuccess.Should().BeTrue();
        result.Value.ConfirmedAt.Should().NotBeNull();
        result.Value.ShippedAt.Should().NotBeNull();
        result.Value.DeliveredAt.Should().NotBeNull();
        result.Value.CompletedAt.Should().NotBeNull();
    }

    #endregion

    #region NotFound Scenarios

    [Fact]
    public async Task Handle_WhenOrderNotFound_ShouldReturnNotFound()
    {
        // Arrange
        var command = CreateTestCommand();

        _orderRepositoryMock
            .Setup(x => x.FirstOrDefaultAsync(
                It.IsAny<OrderByIdForUpdateSpec>(),
                It.IsAny<CancellationToken>()))
            .ReturnsAsync((Order?)null);

        // Act
        var result = await _handler.Handle(command, CancellationToken.None);

        // Assert
        result.IsFailure.Should().BeTrue();
        result.Error.Type.Should().Be(ErrorType.NotFound);
        result.Error.Code.Should().Be("NOIR-ORDER-002");
        result.Error.Message.Should().Contain("not found");

        _unitOfWorkMock.Verify(
            x => x.SaveChangesAsync(It.IsAny<CancellationToken>()),
            Times.Never);
    }

    #endregion

